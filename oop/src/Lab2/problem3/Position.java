package Lab2.problem3;

class Position {
	private int x; 
	private int y; 
	
	public Position() { 
		
	}
	
	public Position(int x, int y ) { 
		this.x = x ; 
		this.y=y; 
	}
	
	public int getX() { 
		return x;
	}
	public int getY() { 
		return y;
	}
	public void setX() { 
		this.x=x;
	}
	public void setY() { 
		this.y=y; 
	}
	
	
}
